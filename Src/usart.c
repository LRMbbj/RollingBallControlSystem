void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)
    {
        if (hmi.byteBUF == 0x70 || hmi.byteBUF == 0x71)
        {
            hmi.state = hmi.byteBUF - 0x70 + 1;   //str->1,int->2
            *hmi.psize = 0;
        }
        else if (hmi.state == 1 && *hmi.psize < UART_BUF_MAX_SIZE)
        {
            if (*hmi.psize > 1 && hmi.UART_BUF[*hmi.psize - 1] == 0xff && hmi.UART_BUF[*hmi.psize - 2] == 0xff && hmi.byteBUF == 0xff)
                {
                    *hmi.psize -= 2;
                    hmi.state = 0; 
                }
            else
            {
                hmi.UART_BUF[*hmi.psize] = hmi.byteBUF;
                (*hmi.psize)++;
            }
        }
        else if (hmi.state == 2 && *hmi.psize < UART_BUF_MAX_SIZE)
        {
            if (*hmi.psize == 6 && hmi.UART_BUF[*hmi.psize - 1] == 0xff && hmi.UART_BUF[*hmi.psize - 2] == 0xff && hmi.byteBUF == 0xff)
                {
                    *hmi.psize -= 2;
                    hmi.state = 0; 
                }
            else
            {
                hmi.UART_BUF[*hmi.psize] = hmi.byteBUF;
                (*hmi.psize)++;
            }
        }
        else
        {
            hmi.state = 0;
            *hmi.psize = 0;
        }
        HAL_UART_Receive_IT(&huart1, &hmi.byteBUF, 1);
    }
    
    if (huart->Instance == USART2)
    {
        posX = posBUF[0];
        posY = posBUF[1];
    }
}
